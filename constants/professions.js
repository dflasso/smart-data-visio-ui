export const buildOptionsToAutocomplete = ({ ltsOptions = [], language = Languages.spanish }) => {
    switch (language) {
        case Languages.english:
            return (
                ltsOptions.map(option => ({
                    ...option,
                    title: option.label_english
                }))
            )
        case Languages.spanish:
        default:
            return (
                ltsOptions.map(option => ({
                    ...option,
                    title: option.label_spanish
                }))
            )
    }
}

export const buildOptionsToAutocompleteFiltere = ({ ltsOptions = [], language = Languages.spanish, callbackFilter }) => {
    if (typeof callbackFilter === "function") {
        switch (language) {
            case Languages.english:

                return ltsOptions
                    .filter(callbackFilter)
                    .map(item => ({
                        ...item,
                        title: item.label_english
                    }))
            case Languages.spanish:
            default:

                return ltsOptions
                    .filter(callbackFilter)
                    .map(item => ({
                        ...item,
                        title: item.label_spanish
                    }))
        }
    } else {
        return []
    }

}

export const findByNameForce = ({ name = "" }) => {
    return forces.find(force => (force.label_english === name || force.label_spanish === name))
}

export const defaultFilterCallbakRanks = ({ name = "" }) => (item) => {

    const idForce = findByNameForce({ name: name }).id
    return item.id_force === idForce
}